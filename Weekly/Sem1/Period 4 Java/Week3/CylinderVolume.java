public class CylinderVolume {
    public static void main(String[] args){
        double Volume;
        double r;
        double h;
        r=5;
        h=12;
        Volume=Math.PI*(r*r)*h;
        System.out.println("The volume of a cylinder with a radius of 5 and a height of 12 is " + Volume);
    }
}
