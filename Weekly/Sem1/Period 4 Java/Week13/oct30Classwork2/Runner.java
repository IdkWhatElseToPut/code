package Week13.oct30Classwork2;

public class Runner {
    public static void main(String[] args){
        for(int i = 1; i<=25; i++){
            System.out.print(i + " ");
        }
        System.out.println("\n");
        for(int i=25;  i>=1; i--){
            System.out.print(i + " ");
        }
    }
}
